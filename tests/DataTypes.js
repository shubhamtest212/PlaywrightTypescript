// Typescript Data Type : string
// A string is used to store textual data such as names, emails, addresses, messages
export let name = 'Shubham';
let name1 = 'Shubham';
console.log(name);
console.log(name1);
// Different ways to create strings
const fName = "Shubham"; // Double quotes
console.log(fName);
const FName = 'shubham'; // Single quotes
console.log(FName);
// Template literals(backticks)
let firstName = "Shubham";
let organization = 'Firstcry.com';
let message = `My name is ${firstName}, I work at ${organization}.`;
console.log(message);
// This is called String Interpolation.
// string concatenation
let firstName1 = "Shubham";
let lastName1 = "Jogdand";
let education = "MCA";
let fullName = firstName1 + "" + lastName1 + "" + education;
console.log(fullName);
